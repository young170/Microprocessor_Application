.thumb
.thumb_func
.global main

.equ GPIO_LED1_MASK, 0x02000
.equ GPIO_P0_BASE, 0x50000000
.equ GPIO_OUT_OFFSET, 0x504
.equ GPIO_OUTCLR_OFFSET, 0x50C
.equ GPIO_DIR_OFFSET, 0x514
.equ GPIO_DIRSET_OFFSET, 0x518


main:
//DIRSET register
LDR R0, =GPIO_P0_BASE+GPIO_DIRSET_OFFSET
LDR R1, =GPIO_LED1_MASK
STR R1, [R0]

// OUT register
LDR R0, =GPIO_P0_BASE+GPIO_OUT_OFFSET
LDR R1, =GPIO_LED1_MASK
MVN R1, #GPIO_LED1_MASK
STR R1, [R0]

loop: b loop